package week1.day1;

public class LearnForLoop {

	public static void main(String[] args) {
		// 1 to 10 // 10 to 1
		// to run the same code multiple time -> For Loop
		//initialize ; condition ; increment / decrement
		for(int i = 10; i >= 1; i--) { // i = 2
			System.out.println(i);
		}
		
	}

}
