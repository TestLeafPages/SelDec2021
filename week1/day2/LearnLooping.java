package week1.day2;

public class LearnLooping {

	public static void main(String[] args) {
		//need to execute code multiple times -> Looping
		
		//Want to print 1 - 10
		
		//initialize;condition;increment/decrement
		for(int i = 1; i <= 10; i++) // i++ -> i=i+1  
		{
			System.out.println(i); //2
		}
		
		

	}

}
